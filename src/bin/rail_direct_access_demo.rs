#![no_std]
#![no_main]
#![allow(warnings)]

use core::ffi::c_void;

use cortex_m::asm;
use defmt_rtt as _; // global logger
use efr32mg22_pac::{self as _, Peripherals};

use efr32_rail::rail::*;

static mut PACKET_RECEIVED: bool = false;

const TX_BUFFER_LENGTH: usize = 16;
static mut RX_FIFO_BUFFER: [u32; 128] = [0; 128];

pub unsafe extern "C" fn event_callback(rail_handle: sl_rail_handle_t, event: sl_rail_events_t) {
    // ... handle RAIL events, e.g., receive and transmit completion
    if event == SL_RAIL_EVENT_RX_PACKET_RECEIVED.into() {
        unsafe {
            PACKET_RECEIVED = true;
        }

        // keep packet info in memory so that we can also read it later in the main loop
        unsafe { sl_rail_hold_rx_packet(rail_handle) };
    }
}

pub unsafe extern "C" fn init_callback(_c: RAIL_Handle_t) {
    defmt::info!("successfully initialized radio");
}

unsafe fn initialize_radio() -> sl_rail_handle_t {
    let p_rail_config = sl_rail_config {
        events_callback: Some(event_callback),
        p_opaque_handle1: core::ptr::null_mut(),
        p_opaque_handle2: core::ptr::null_mut(),
        opaque_value: [0],
        rx_packet_queue_entries: SL_RAIL_BUILTIN_RX_PACKET_QUEUE_ENTRIES as u16,
        rx_fifo_bytes: SL_RAIL_BUILTIN_RX_FIFO_BYTES as u16,
        tx_fifo_bytes: TX_BUFFER_LENGTH as u16,
        tx_fifo_init_bytes: 0,
        p_rx_packet_queue: sl_rail_builtin_rx_packet_queue_ptr,
        p_rx_fifo_buffer: sl_rail_builtin_rx_fifo_ptr,
        p_tx_fifo_buffer: &mut RX_FIFO_BUFFER[0],
    };
    sl_rail_util_pa_init();

    // https://github.com/SiliconLabs/simplicity_sdk/blob/b41bec3ff2485199c1a5a9995b3e649e118c1b8d/platform/radio/rail_lib/common/sl_rail_types.h#L119
    let mut p_rail_handle = 0xFFFF_FFFF as *mut c_void;
    let status = sl_rail_init(
        (&mut p_rail_handle) as *mut *mut c_void,
        &p_rail_config,
        Some(init_callback),
    );
    assert_eq!(status, RAIL_STATUS_NO_ERROR);

    // initialize radio calibration
    let status = sl_rail_config_cal(p_rail_handle, SL_RAIL_CAL_ALL);
    assert_eq!(status, RAIL_STATUS_NO_ERROR);

    // configuration is read from `rail_config.c`, which is auto-generated by Simplicity Studio
    let p_channel_config =
        &Protocol_Configuration_channelConfig as *const _ as *const sl_rail_channel_config;
    let status = sl_rail_config_channels(
        p_rail_handle,
        p_channel_config,
        Some(sl_rail_util_pa_on_channel_config_change),
    );
    assert_eq!(status, RAIL_STATUS_NO_ERROR);

    // set power to use for sending packets
    let status = RAIL_SetTxPowerDbm(p_rail_handle, 20 /* 20 dBm */);
    assert_eq!(status, RAIL_STATUS_NO_ERROR);

    // Configure the most useful callbacks and catch a few errors.
    let status = sl_rail_config_events(
        p_rail_handle,
        SL_RAIL_EVENTS_ALL as u64,
        (SL_RAIL_EVENT_TX_PACKET_SENT
            | SL_RAIL_EVENT_RX_PACKET_RECEIVED
            | SL_RAIL_EVENT_RX_FRAME_ERROR) as u64,
    );
    assert_eq!(status, RAIL_STATUS_NO_ERROR);

    // automatically transition back to receive mode after a rx/tx operation has finished
    let p_state_transitions = sl_rail_state_transitions {
        success: SL_RAIL_RF_STATE_RX as u8,
        error: SL_RAIL_RF_STATE_RX as u8,
    };
    let status = sl_rail_set_rx_transitions(p_rail_handle, &p_state_transitions);
    assert_eq!(status, RAIL_STATUS_NO_ERROR);
    let status = sl_rail_set_tx_transitions(p_rail_handle, &p_state_transitions);
    assert_eq!(status, RAIL_STATUS_NO_ERROR);

    p_rail_handle
}

unsafe fn configure_clocks(peripherals: &Peripherals) {
    // enable HFXO oscillator
    peripherals.cmu_ns.clken0().write(|w| w.hfxo0().set_bit());
    peripherals.hfxo0_ns.ctrl().write(|w| w.forceen().set_bit());

    // wait until the clock finished starting
    while peripherals.hfxo0_ns.status().read().rdy().bit_is_clear() {}

    // set sysclk source to HFXO Clock
    peripherals.cmu_ns.sysclkctrl().write(|w| w.clksel().hfxo());
}

#[cortex_m_rt::entry]
fn main() -> ! {
    let peripherals = Peripherals::take().unwrap();
    unsafe {
        configure_clocks(&peripherals);

        let p_rail_handle = initialize_radio();

        // start listening for incoming packets
        let channel = sl_rail_get_first_channel(p_rail_handle, core::ptr::null());
        let status = sl_rail_start_rx(p_rail_handle, channel, core::ptr::null());
        assert_eq!(status, RAIL_STATUS_NO_ERROR);

        loop {
            // periodically run the loop every 40mio cycles
            asm::delay(40_000_000);
            send_packet(p_rail_handle);

            if PACKET_RECEIVED {
                PACKET_RECEIVED = false;
                read_received_packet(p_rail_handle);
            }
        }
    }
}

unsafe fn send_packet(p_rail_handle: sl_rail_handle_t) {
    // some random bytes to send
    let out_packet: [u8; _] = [
        0x0F, 0x16, 0x11, 0x22, 0x33, 0x44, 0x55, 0x66, 0x77, 0x88, 0x99, 0xAA, 0xBB, 0xCC, 0xDD,
        0xEE,
    ];

    // prepare packet for being sent by copying it into the write queue
    let bytes_written =
        sl_rail_write_tx_fifo(p_rail_handle, &out_packet[0], TX_BUFFER_LENGTH as u16, true);
    assert_eq!(bytes_written, TX_BUFFER_LENGTH as u16);

    let channel = sl_rail_get_first_channel(p_rail_handle, core::ptr::null());
    let status = sl_rail_start_tx(
        p_rail_handle,
        channel,
        SL_RAIL_TX_OPTIONS_DEFAULT,
        core::ptr::null(),
    );
    assert_eq!(status, SL_RAIL_STATUS_NO_ERROR);
    defmt::info!("successfully sent packet");
}

// Ported from the official TRX example app.
unsafe fn read_received_packet(p_rail_handle: sl_rail_handle_t) {
    // will be overriden by sl_rail_get_rx_packet_info, so content doesn't matter
    let mut p_packet_info = sl_rail_rx_packet_info {
        packet_status: 0,
        packet_bytes: 0,
        first_portion_bytes: 0,
        p_first_portion_data: core::ptr::null_mut(),
        p_last_portion_data: core::ptr::null_mut(),
        filter_mask: 0,
    };

    let packet_handle = sl_rail_get_rx_packet_info(
        p_rail_handle,
        // https://github.com/SiliconLabs/simplicity_sdk/blob/sisdk-2025.6/platform/radio/rail_lib/common/rail_types.h#L4190 SL_RAIL_RX_PACKET_HANDLE_OLDEST_COMPLETE,
        2 as RAIL_RxPacketHandle_t,
        &mut p_packet_info,
    );
    assert!(!packet_handle.is_null());

    let mut input_buffer: [u8; TX_BUFFER_LENGTH] = [0; TX_BUFFER_LENGTH];
    let status =
        sl_rail_copy_rx_packet(p_rail_handle, &mut input_buffer as *mut u8, &p_packet_info);
    assert_eq!(status, SL_RAIL_STATUS_NO_ERROR);
    defmt::info!("received buffer: {:X}", input_buffer);

    let status = sl_rail_release_rx_packet(p_rail_handle, packet_handle);
    assert_eq!(status, SL_RAIL_STATUS_NO_ERROR);
}

#[panic_handler]
fn panic(_info: &core::panic::PanicInfo) -> ! {
    defmt::error!("panicked");
    loop {
        cortex_m::asm::bkpt();
    }
}
